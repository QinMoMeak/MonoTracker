      $lines[$i] = "${indent}${key}: { 'zh-CN': '${zhCN}', 'zh-TW': '${zhTW}', 'en': '${enVal}', 'ja': '${ja}' },"
    }
  }
}

Set-Content -Encoding utf8 -Path $path -Value ($lines -join "`n")
